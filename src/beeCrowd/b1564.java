package beeCrowd;

import java.util.Scanner;

public class b1564 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) {
            int N = sc.nextInt();
            if (N >= 1) {
                System.out.println("vai ter duas!");
            } else {
                System.out.println("vai ter copa!");
            }
        }
    }
}
